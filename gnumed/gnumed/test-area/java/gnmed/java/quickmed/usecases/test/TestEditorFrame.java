    /** Creates new form TestEditorFrame */
    public TestEditorFrame() {
        initComponents();
        Class[] cc = getEditorKit().getClass().getClasses();
        for (int i = 0; i < cc.length; ++i ) {
            if (Action.class.isAssignableFrom(cc[i])) {
                try {
                    jToolBar1.add( new JButton( (Action) cc[i].newInstance()));
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        Action[] actions = jEditorPane1.getEditorKit().getActions();
        ActionMap map = new ActionMap();
        for (int i = 0; i < actions.length; ++i) {
            
            map.put(actions[i].NAME, actions[i]);
           
        }
        
        jToolBar1.setActionMap(map);
        
    }
    
